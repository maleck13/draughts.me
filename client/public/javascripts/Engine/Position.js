Position = function(x, y)
{
    this.x = x;
    this.y = y;
}

Position.prototype.getX = function()
{
    return this.x;
}

Position.prototype.getY = function()
{
    return this.y;
}

Position.prototype.setX = function(x)
{
    this.x = x;
}

Position.prototype.setY = function(y)
{
    this.y = y;
}

